package welchezukunft;

public class colorGradient {
    private	int r1, r2, g1, g2, b1, b2, fc1, fc2;
 
	public colorGradient(int r1, int r2, int g1, int g2, int b1, int b2, int fc1, int fc2) {
		this.r1 = r1;
		this.r2 = r2;
		this.g1 = g1;
		this.g2 = g2;
		this.b1 = b1;
		this.b2 = b2;
		this.fc1 = fc1;
		this.fc2 = fc2;
	}

	public int getR1() {
		return r1;
	}

	public void setR1(int r1) {
		this.r1 = r1;
	}

	public int getR2() {
		return r2;
	}

	public void setR2(int r2) {
		this.r2 = r2;
	}

	public int getG1() {
		return g1;
	}

	public void setG1(int g1) {
		this.g1 = g1;
	}

	public int getG2() {
		return g2;
	}

	public void setG2(int g2) {
		this.g2 = g2;
	}

	public int getB1() {
		return b1;
	}

	public void setB1(int b1) {
		this.b1 = b1;
	}

	public int getB2() {
		return b2;
	}

	public void setB2(int b2) {
		this.b2 = b2;
	}

	public int getFc1() {
		return fc1;
	}

	public void setFc1(int fc1) {
		this.fc1 = fc1;
	}

	public int getFc2() {
		return fc2;
	}

	public void setFc2(int fc2) {
		this.fc2 = fc2;
	}
	
	
	
}
